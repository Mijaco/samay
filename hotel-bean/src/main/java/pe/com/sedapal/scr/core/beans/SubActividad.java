package pe.com.sedapal.scr.core.beans;

public class SubActividad {

	private Integer idSubActividad;
	private String descSubActividad;
	private Integer estado;
	
	public Integer getIdSubActividad() {
		return idSubActividad;
	}
	public void setIdSubActividad(Integer idSubActividad) {
		this.idSubActividad = idSubActividad;
	}
	public String getDescSubActividad() {
		return descSubActividad;
	}
	public void setDescSubActividad(String descSubActividad) {
		this.descSubActividad = descSubActividad;
	}
	public Integer getEstado() {
		return estado;
	}
	public void setEstado(Integer estado) {
		this.estado = estado;
	}
	
}
